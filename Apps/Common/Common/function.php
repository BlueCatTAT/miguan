/**
 * 检测用户是否登录
 * @return integer 0-未登录，大于0-当前登录用户ID
 * @author 麦当苗儿 <zuojiazi@vip.qq.com>
 */
function is_login() {
    $user = session('user_auth');
    if (empty($user)) {
        return 0;
    } else {
        $uid = session('user_auth_sign') == data_auth_sign($user) ? $user['uid'] : 0;
        $Member = M('Member');
        if ($Member->where(['id' => $uid])->find()) {
            return $uid;
        } else {
            return 0;
        }
    }
}

